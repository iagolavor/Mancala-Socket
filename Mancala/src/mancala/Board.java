    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if((portField.getText().length()>0) && (hostField.getText().length()>0)){
            try{
                this.mancala = new Mancala(new Socket(hostField.getText(),Integer.parseInt(portField.getText())), this);
                Thread t = new Thread(this.mancala);
                t.start();
                this.onlineCheckbox.setSelected(true);
                this.playerLabel.setText(this.mancala.getPlayer());
                //Adicionando movimento depois do cliente ser criado.
                this.addMovement(this.pieces);
                this.hostNameLabel.setText(this.mancala.getHostName());
            } catch (IOException ex) {
                Logger.getLogger(Board.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

