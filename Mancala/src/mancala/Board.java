    private JLabel[] registerPieces(Component[] arr){
        JLabel[] labs = new JLabel[48];
        for(int i=0; i<arr.length; i++){
            if(arr[i] instanceof JLabel){
               labs[i] = (JLabel) arr[i];
               this.startingX[i] = labs[i].getX();
               this.startingY[i] = labs[i].getY();
            }
        }
        return labs;
    }
    
    private void resetBoard(){
        for(int i=0; i<this.pieces.length; i++){
            this.setPiece(i, this.startingX[i], this.startingY[i]);
        }
    }
    
    private void namePieces(JLabel[] arr){
        int i=0;
        for(JLabel j : arr){
            j.setName(Integer.toString(i));
            i++;
        }
    }
    
